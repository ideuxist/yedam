package com.dev;

public class PersonalTraining {

	public static void main(String[] args) {
		// 1부터 100 정수 3배수의 총합
		for (int i = 1; i < 5; i++) {
			for (int j = 4; j > 0; j--) {

				if (i < j) {
					System.out.print(" ");
				} else {
					System.out.print("*");
				}
			}
			System.out.println();
			
			}
			
		}

	}

